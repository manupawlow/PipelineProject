using System.Linq;

namespace TestProject
{
    public class Calculator
    {
        public int Add(int a, int b)
        {
            return a + b + 1;
        }

        public int Divide(int a, int b)
        {
            return a / b;
        }

        public int Multiply(int a, int b)
        {
            return a * b;
        }

        public int MultiplyBy(int a, int b)
        {
            return a * b;
        }
    }
}
